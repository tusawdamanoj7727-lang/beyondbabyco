#!/usr/bin/env node
/**
 * Combines supabase/database/00N_*.sql files into one script for the
 * Supabase SQL Editor (when Supabase CLI is not available).
 *
 * Usage: npm run db:combine
 * Output: supabase/database/APPLY_ALL.sql
 */
import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const migrationDir = join(root, "supabase/database");
const outFile = join(migrationDir, "APPLY_ALL.sql");

const files = readdirSync(migrationDir)
  .filter((f) => /^\d{3}_.+\.sql$/.test(f))
  .sort();

if (files.length === 0) {
  console.error("No migration files found in supabase/database/");
  process.exit(1);
}

const header = `-- =====================================================================
-- BeyondBabyCo — COMBINED DATABASE SETUP
-- Generated by: npm run db:combine
-- Migrations: ${files.length} files (001–${files[files.length - 1].slice(0, 3)})
--
-- HOW TO APPLY (Supabase Dashboard):
--   1. Open your project → SQL Editor → New query
--   2. Paste this entire file
--   3. Run once on a fresh project (or re-run safely — migrations are idempotent)
--
-- DO NOT edit this file by hand — regenerate with npm run db:combine
-- Source files live in supabase/database/
-- =====================================================================

`;

const parts = [header];

for (const file of files) {
  const content = readFileSync(join(migrationDir, file), "utf-8").trimEnd();
  parts.push(
    `\n-- =====================================================================\n-- FILE: ${file}\n-- =====================================================================\n\n`,
    content,
    "\n",
  );
}

writeFileSync(outFile, parts.join(""), "utf-8");

console.log(`Combined ${files.length} migrations → supabase/database/APPLY_ALL.sql`);
console.log("Apply via Supabase Dashboard → SQL Editor → paste → Run");
