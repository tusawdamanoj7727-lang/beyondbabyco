#!/usr/bin/env node
/**
 * Phase 10.0B — Database migration repair workflow.
 *
 * Audit → repair SQL → sync migrations → re-audit → validation → report
 *
 * Usage: npm run repair:database
 */
import { execSync, spawnSync } from "node:child_process";
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

import {
  MIGRATION_SENTINELS,
  REPAIR_MIGRATION_RANGE,
  listMigrationFiles,
  migrationNumber,
  migrationsInRange,
  resolveDatabaseUrl,
} from "./lib/database-migrations.mjs";
import { hasOnConflict, parseInsertTable, parseOnConflict, splitSqlStatements, truncateStatement } from "./lib/migration-sql.mjs";
import { loadEnvFile } from "./env-config.mjs";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const migrationDir = join(root, "supabase/database");
const auditPath = join(root, "scripts/.database-audit.json");
const syncReportPath = join(root, "scripts/.database-sync-report.json");
const repairSqlPath = join(root, "scripts/database-repair.sql");
const repairReportPath = join(root, "docs/MIGRATION_REPAIR_REPORT.md");
const finalStatusPath = join(root, "docs/FINAL_DATABASE_STATUS.md");

function run(cmd, opts = {}) {
  return spawnSync(cmd, { shell: true, cwd: root, encoding: "utf8", ...opts });
}

function runOrLog(cmd) {
  console.log(`\n$ ${cmd}`);
  const r = run(cmd);
  if (r.stdout) process.stdout.write(r.stdout);
  if (r.stderr) process.stderr.write(r.stderr);
  return r.status ?? 1;
}

/** Static scan of migration files for ON CONFLICT usage. */
function scanMigrationsForConflicts(files) {
  const findings = [];
  for (const file of files) {
    const sql = readFileSync(join(migrationDir, file), "utf8");
    for (const statement of splitSqlStatements(sql)) {
      if (!hasOnConflict(statement)) continue;
      const table = parseInsertTable(statement);
      const conflict = parseOnConflict(statement);
      findings.push({
        migration: file,
        table,
        columns: conflict?.columns ?? ["PRIMARY KEY"],
        statement: truncateStatement(statement),
        risk:
          table === "media_folders" && conflict?.columns?.includes("slug")
            ? "fixed_in_source_use_where_not_exists"
            : "verify_unique_constraint",
      });
    }
  }
  return findings;
}

/** Build repair SQL (constraints only — no drops, no table recreation). */
function buildRepairSql(conflictFindings) {
  const lines = [
    "-- BeyondBabyCo — database-repair.sql",
    "-- Generated by npm run repair:database",
    "-- Additive only: missing indexes/constraints. No DROP. No data changes.",
    "",
  ];

  const needsMediaSlugNote = conflictFindings.some(
    (f) => f.table === "media_folders" && f.columns?.includes("slug"),
  );

  if (needsMediaSlugNote) {
    lines.push(
      "-- 009_media_library.sql: seed insert fixed to WHERE NOT EXISTS (partial unique index",
      "-- on media_folders.slug is incompatible with ON CONFLICT (slug)).",
      "-- Ensure slug column + partial unique index exist if 009 halted mid-run:",
      "",
      "alter table public.media_folders",
      "  add column if not exists slug text;",
      "",
      "create unique index if not exists uq_media_folders_slug",
      "  on public.media_folders (slug)",
      "  where slug is not null;",
      "",
    );
  }

  lines.push("-- No additional UNIQUE constraints required for migrations 007–021.");
  lines.push("-- permissions.code, homepage_sections.key, marketing_segments.slug already UNIQUE in schema.");
  lines.push("");

  return lines.join("\n");
}

/** Generate grouped migration repair report markdown. */
function buildMigrationRepairReport(audit, conflictFindings, syncReport) {
  const missing = audit?.missingMigrations ?? [];
  const rangeFiles = migrationsInRange(listMigrationFiles(migrationDir), REPAIR_MIGRATION_RANGE.from, REPAIR_MIGRATION_RANGE.to);

  const sections = rangeFiles.map((file) => {
    const sentinel = MIGRATION_SENTINELS.find((s) => s.file === file);
    const status = missing.includes(file) ? "missing" : "applied";
    const conflicts = conflictFindings.filter((f) => f.migration === file);
    const syncErrors = syncReport?.errors?.filter((e) => e.migration === file) ?? [];
    const syncSkipped = syncReport?.skippedStatements?.filter((e) => e.migration === file) ?? [];

    return {
      file,
      status,
      sentinel,
      conflicts,
      syncErrors,
      syncSkipped,
    };
  });

  let md = `# Migration Repair Report — Phase 10.0B

**Generated:** ${new Date().toISOString()}  
**Project:** ${audit?.projectUrl ?? "unknown"}

## Root cause

\`APPLY_ALL.sql\` failed with **SQLSTATE 42P10** — \`ON CONFLICT\` target had no matching UNIQUE constraint.

| Migration | Issue | Fix |
|-----------|-------|-----|
| \`009_media_library.sql\` | \`ON CONFLICT (slug)\` on \`media_folders\` — only a **partial** unique index exists | Rewritten to \`WHERE NOT EXISTS\` (no functionality change) |

## Applied vs missing (007–021)

| Migration | Status |
|-----------|--------|
${sections.map((s) => `| ${s.file} | ${s.status === "applied" ? "✅ applied" : "❌ missing"} |`).join("\n")}

## ON CONFLICT audit (007–021)

| Migration | Table | Columns | Risk |
|-----------|-------|---------|------|
${conflictFindings.map((f) => `| ${f.migration} | ${f.table ?? "—"} | ${f.columns?.join(", ") ?? "PK"} | ${f.risk} |`).join("\n")}

## Grouped by migration

`;

  for (const s of sections) {
    md += `### ${s.file} — ${s.status}\n\n`;
    if (s.sentinel?.table) {
      md += `- **Sentinel:** \`${s.sentinel.table}.${s.sentinel.column}\`\n`;
    } else if (s.sentinel?.pgFunction) {
      md += `- **Sentinel:** \`${s.sentinel.pgFunction}()\`\n`;
    } else if (s.sentinel?.permissionCode) {
      md += `- **Sentinel:** permission \`${s.sentinel.permissionCode}\`\n`;
    }
    if (s.conflicts.length) {
      md += `- **ON CONFLICT statements:** ${s.conflicts.length}\n`;
      for (const c of s.conflicts) {
        md += `  - \`${c.table}\` (${c.columns.join(", ")}) — ${c.risk}\n`;
      }
    }
    if (s.syncErrors.length) {
      md += `- **Sync errors:**\n`;
      for (const e of s.syncErrors) {
        md += `  - \`${e.sqlState}\`: ${e.message}\n`;
      }
    }
    if (s.syncSkipped.length) {
      md += `- **Skipped statements:** ${s.syncSkipped.length}\n`;
    }
    md += "\n";
  }

  md += `## Missing schema objects (from audit)

### Tables
${(audit?.missingTables ?? []).map((t) => `- \`${t}\``).join("\n") || "- none"}

### Functions (REST/pg)
${(audit?.missingFunctions ?? []).map((f) => `- \`${f}()\``).join("\n") || "- none"}

## Repair artifacts

- \`scripts/database-repair.sql\` — additive constraints/indexes only
- \`009_media_library.sql\` — seed insert uses \`WHERE NOT EXISTS\`
- \`npm run sync:database -- --from=007\` — statement-level sync with 42P10 skip

## Next steps

1. \`npm run db:combine\` — regenerate APPLY_ALL.sql with 009 fix
2. Paste **APPLY_ALL.sql** in Supabase SQL Editor **OR** \`npm run sync:database -- --from=007\` with \`DATABASE_URL\`
3. \`npm run audit:database\` — expect 23/23
4. \`npm run test:e2e\` — target 9/9
`;

  return md;
}

function buildFinalStatus(audit, validation, e2eStatus) {
  const missing = audit?.missingMigrations?.length ?? "?";
  const applied = audit?.appliedMigrations?.length ?? "?";

  return `# Final Database Status — Phase 10.0B

**Generated:** ${new Date().toISOString()}

## Migration sync

| Metric | Value |
|--------|------:|
| Applied | ${applied} |
| Missing | ${missing} |
| First gap | ${audit?.firstMissingMigration ?? "none"} |

## Validation

| Command | Status |
|---------|--------|
| \`npm run validate:migrations\` | ${validation.validateMigrations ?? "—"} |
| \`npm run audit:database\` | ${validation.auditDatabase ?? "—"} |
| \`npm run check:admin\` | ${validation.checkAdmin ?? "—"} |
| \`npm run lint\` | ${validation.lint ?? "—"} |
| \`npm run typecheck\` | ${validation.typecheck ?? "—"} |
| \`npm run test\` | ${validation.test ?? "—"} |
| \`npm run build\` | ${validation.build ?? "—"} |
| \`npm run test:e2e\` | ${e2eStatus ?? "—"} |

## E2E target

**9/9** after migrations 007–021 are applied.

## Fixes applied in Phase 10.0B

1. \`009_media_library.sql\` — replaced invalid \`ON CONFLICT (slug)\` with \`WHERE NOT EXISTS\`
2. \`scripts/sync-database.mjs\` — statement-level execution, 42P10 skip, sync report JSON
3. \`scripts/repair-database.mjs\` — full repair workflow
4. \`scripts/database-repair.sql\` — additive constraint repair

## Remaining action

${missing === 0 || missing === "0" ? "✅ Database fully synchronized." : "⚠️ Apply migrations 007–021 via SQL Editor or `npm run sync:database -- --from=007`."}
`;
}

console.log("\nBeyondBabyCo — Phase 10.0B Database Repair\n");

// Step 1: combine (ensure APPLY_ALL reflects 009 fix)
runOrLog("npm run db:combine");

// Step 2: audit
const auditExit = runOrLog("npm run audit:database");
let audit = existsSync(auditPath) ? JSON.parse(readFileSync(auditPath, "utf8")) : null;

// Step 3: static ON CONFLICT scan
const rangeFiles = migrationsInRange(listMigrationFiles(migrationDir), REPAIR_MIGRATION_RANGE.from, REPAIR_MIGRATION_RANGE.to);
const conflictFindings = scanMigrationsForConflicts(rangeFiles);

// Step 4: write database-repair.sql
const repairSql = buildRepairSql(conflictFindings);
writeFileSync(repairSqlPath, repairSql);
console.log(`\n✓ Wrote ${repairSqlPath}`);

// Step 5: apply repair + sync if DATABASE_URL available
const env = loadEnvFile(resolve(root, ".env.local"), readFileSync, existsSync);
const dbUrl = resolveDatabaseUrl(env);
let syncReport = null;

if (dbUrl) {
  console.log("\n── Applying repair SQL + migrations ──\n");
  try {
    const { default: pg } = await import("pg");
    const client = new pg.Client({ connectionString: dbUrl, ssl: { rejectUnauthorized: false } });
    await client.connect();
    await client.query(readFileSync(repairSqlPath, "utf8"));
    await client.end();
    console.log("✓ database-repair.sql applied");
  } catch (err) {
    console.error(`✗ repair SQL: ${err instanceof Error ? err.message : String(err)}`);
  }
  runOrLog("npm run sync:database -- --from=007");
  if (existsSync(syncReportPath)) {
    syncReport = JSON.parse(readFileSync(syncReportPath, "utf8"));
  }
  runOrLog("npm run audit:database");
  audit = existsSync(auditPath) ? JSON.parse(readFileSync(auditPath, "utf8")) : audit;
} else {
  console.log("\n⚠ DATABASE_URL not set — skipping live sync (reports + fixed migrations generated)\n");
}

// Step 6: validation (app code — no business logic changes)
const validation = {};
validation.validateMigrations = runOrLog("npm run validate:migrations") === 0 ? "✅" : "❌";
validation.lint = runOrLog("npm run lint") === 0 ? "✅" : "⚠️ warnings";
validation.typecheck = runOrLog("npm run typecheck") === 0 ? "✅" : "❌";
validation.test = runOrLog("npm run test") === 0 ? "✅" : "❌";
validation.build = runOrLog("npm run build") === 0 ? "✅" : "❌";

validation.auditDatabase = existsSync(auditPath)
  ? JSON.parse(readFileSync(auditPath, "utf8")).missingMigrations?.length === 0
    ? "✅"
    : "⚠️ gaps remain"
  : "—";

validation.checkAdmin = runOrLog("npm run check:admin") === 0 ? "✅" : "⚠️";

let e2eStatus = "⏭ skipped (no DATABASE_URL or schema gaps)";
if (dbUrl && audit?.missingMigrations?.length === 0) {
  const e2e = run(
    "E2E_ADMIN_EMAIL=admin@beyondbabyco.com E2E_ADMIN_PASSWORD=Admin@123456 npm run test:e2e",
  );
  e2eStatus = e2e.status === 0 ? "✅ 9/9" : `⚠️ exit ${e2e.status}`;
}

// Step 7: reports
writeFileSync(repairReportPath, buildMigrationRepairReport(audit, conflictFindings, syncReport));
writeFileSync(finalStatusPath, buildFinalStatus(audit, validation, e2eStatus));

console.log(`\n✓ ${repairReportPath}`);
console.log(`✓ ${finalStatusPath}\n`);

process.exit((audit?.missingMigrations?.length ?? 0) > 0 ? 1 : 0);
